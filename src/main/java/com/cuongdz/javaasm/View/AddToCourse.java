    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        // Check selected index
        if (cboClass.getSelectedIndex() < 1) {
            lbNotify.setText("Please Select!");
            return;
        }
        // Check class room availibility
        if (!mc.isClassEnoughSpace(UserIO.getSelectedID(cboClass))) {
            lbNotify.setText("Class full!");
            return;
        }
        // Check student already in class
        if(MySQLUtility.isInClass(UserIO.getSelectedID(cboClass), studentid)){
            lbNotify.setText("Student already in this class!");
            return;
        }else{
        // Update to database
        MySQLUtility.addToClass(UserIO.getSelectedID(cboClass), studentid,courseid);
        JOptionPane.showMessageDialog(null, "Add Successfully!");     
        }

        this.dispose();
    }//GEN-LAST:event_btnConfirmActionPerformed


