    private void getClassItem() {
        ResultSet rs = MySQLUtility.getTCTable();
        try {
            while (rs.next()) {
                courseid = rs.getString("tc.courseid");
                cboClass.addItem(rs.getString("tc.TCID") + "- " + rs.getString("c.Name") + " " + rs.getString("tc.DateCreated"));
            }
        } catch (Exception ex) {

        }
    }

    private String getClassSelectedID(String s) {
        if (cboClass.getSelectedIndex() > 0) {
            return s.substring(0, s.indexOf("-"));
        } else {
            return "";
        }
    }

