    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (cboCourse.getSelectedIndex() > 0 && cboTeacher.getSelectedIndex() > 0) {
            String c = UserIO.getSelectedStringID(cboCourse);
            int t = UserIO.getSelectedID(cboCourse);
            if (!mc.addClass(t, c)) {
                lbNotify.setText("Teaching limited!(5 Classes)");
            } else {
                lbNotify.setText("Added");
                this.dispose();
            }

        } else {
            lbNotify.setText("Please select!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void cboCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCourseActionPerformed
        cboTeacher.removeAllItems();
        // Check course major Id
        String coursedid = UserIO.getSelectedStringID(cboCourse);
        ResultSet rs = MySQLUtility.getTable("courses", "courseid", coursedid);
        try {
            while (rs.next()) {
                getTeacherItem(rs.getInt("majorid"));
            }
        } catch (Exception ex) {
            
        }

    }//GEN-LAST:event_cboCourseActionPerformed

