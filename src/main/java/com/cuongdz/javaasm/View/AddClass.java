    private void getCourseItem() {
        ResultSet rs = MySQLUtility.getTable("courses");
        try {
            while (rs.next()) {
                cboCourse.addItem(rs.getString("CourseID") + "- " + rs.getString("Name"));
            }
        } catch (Exception ex) {

        }
    }

    private void getTeacherItem(int majorid) {
        ResultSet rs = MySQLUtility.getTable("teachers", "majorid", majorid + "");
        try {
            while (rs.next()) {
                cboTeacher.addItem(rs.getString("TeacherID") + "- " + rs.getString("firstname") + " " + rs.getString("lastname"));
            }
        } catch (Exception ex) {

        }
    }


